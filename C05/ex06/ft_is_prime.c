int	ft_is_prime(int nb)
{
	long	i;

	if ((nb != 2 && nb % 2 == 0) || nb < 2)
		return (0);
	i = 2;
	while (i * i <= nb)
	{
		if (nb % i++ == 0)
			return (0);
	}
	return (1);
}
